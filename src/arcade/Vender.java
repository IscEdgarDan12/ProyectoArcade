    public void tablaCompraVenta() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo producto");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Canitdad");
        modelo.addColumn("Precio");
        VentaRentaPro.setModel(modelo);
        String sql;
        sql = "select idVenta, productos.Nombre, DescripcionPro, cantidad, precio from venta inner join productos on FKProducto=idProducto order by idVenta;";
        String[] datos = new String[5];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                System.out.println(datos[4]);
                modelo.addRow(datos);
            }
            VentaRentaPro.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println("Error " + ex);
        }
    }

    public void mostrarInfo() {
        int filas = VentaRentaPro.getSelectedRow();
        if (filas >= 0) {
            producto.setText("Producto: " + (VentaRentaPro.getValueAt(filas, 1).toString()));
            precio.setText("Precio: $" + VentaRentaPro.getValueAt(filas, 4).toString() + " Pesos");
            total.setText("Total a pagar: $" + VentaRentaPro.getValueAt(filas, 4).toString() + " Pesos");
        } else {
            JOptionPane.showMessageDialog(null, "No hay filas seleccionadas");
        }

    }

    public void pagoUnico() {
        try {
            int filas = VentaRentaPro.getSelectedRow();
            String cantidad = "";
            String idVenta = "";
            if (filas >= 0) {
                idVenta = ((VentaRentaPro.getValueAt(filas, 0).toString()));
                cantidad = ((VentaRentaPro.getValueAt(filas, 3).toString()));

            }
            System.out.println("valor de cantidad antes de restar: " + cantidad);
            int cantidadF = Integer.parseInt(cantidad);
            cantidadF = cantidadF - 1;
            cantidad = String.valueOf(cantidadF);
            System.out.println("valor de cantidad: " + cantidad);
            String update = "UPDATE venta SET Cantidad = '" + cantidad + "' where venta.idVenta='"+idVenta+"'";
            PreparedStatement pst = cn.prepareStatement(update);
            pst.executeUpdate();
        } catch (Exception e) {
            System.out.println("Error al Actulizar la Cantidad " + e.getMessage());
        }
    }

