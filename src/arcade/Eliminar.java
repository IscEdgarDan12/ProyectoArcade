    public void mostrarTablaEliminar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Codigo del producto");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        tablaEliminar.setModel(modelo);
        String select = "";
        select = "select productos.Nombre, productos.CodigoPro, productos.DescripcionPro, venta.Cantidad, venta.Precio from productos inner join venta on venta.FKProducto= productos.idProducto;";
        try {
            String[] datos = new String[5];
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(select);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                modelo.addRow(datos);
            }
            tablaEliminar.setModel(modelo);
        } catch (Exception e) {
            System.out.println("Error en la tabla Eliminar " + e.getMessage());
        }
    }

    public void eliminarTodo() {
        int confirmarEliminar = JOptionPane.showConfirmDialog(null, "Estas Seguro(a) de eliminar este producto?");
        if (confirmarEliminar == JOptionPane.YES_OPTION) {
            try {
                String deletePro = "";
                String deleteVen = "";
                String codigoPro = "";

                int filas = tablaEliminar.getSelectedRow();
                if (filas >= 0) {
                    codigoPro = ((tablaEliminar.getValueAt(filas, 1).toString()));
                }
                deleteVen = "delete from venta where venta.FKProducto=(select productos.idProducto from productos where productos.CodigoPro='" + codigoPro + "');";
                deletePro = "delete from productos where idProducto=(select productos.idProducto from productos where productos.CodigoPro='" + codigoPro + "');";

                PreparedStatement pst = cn.prepareStatement(deleteVen);
                PreparedStatement pst2 = cn.prepareStatement(deletePro);
                pst.executeUpdate();
                pst2.executeUpdate();

            } catch (NumberFormatException | SQLException e) {
                System.out.println("Error en eliminar un producto " + e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Productos eliminados");
        } else if (confirmarEliminar == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "Solicitud cancelada");
        } else {
            JOptionPane.showMessageDialog(null, "Productos no eliminados");
        }

    }

