    public void registro() {
        try {
            if (nombre.getText().equals("") || apellido.getText().equals("") || usuario.getText().equals("") || String.valueOf(contra.getPassword()).equals("")) {
                JOptionPane.showMessageDialog(null, "Registro no completado favor de llenar todos los campos");
            } else {
                PreparedStatement pst = cn.prepareStatement("INSERT INTO login (idLogin, Nombre, Apellido, Usuario, Contra) VALUES (NULL,?,?,?,?)");
                pst.setString(1, nombre.getText());
                pst.setString(2, apellido.getText());
                pst.setString(3, usuario.getText());
                pst.setString(4, String.valueOf(contra.getPassword()));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registro completado");
                ventanaLogin();
            }

        } catch (Exception e) {
            System.out.println("Error a agregar un dato" + e.getMessage());
        }

    }

