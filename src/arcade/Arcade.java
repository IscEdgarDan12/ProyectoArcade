    public void rentaTeEsta() {
        try {
            String idVenta = "";
            String update = "";
            String cantidad = "";

            int filas = VentaRentaPro.getSelectedRow();
            if (filas >= 0) {
                idVenta = ((VentaRentaPro.getValueAt(filas, 0).toString()));
                cantidad = ((VentaRentaPro.getValueAt(filas, 3).toString()));

            }
            int cantidadF = Integer.parseInt(cantidad);
            cantidadF = cantidadF - 1;
            cantidad = String.valueOf(cantidadF);
            String insert = "";
            update = "UPDATE venta SET Cantidad = '" + cantidad + "' where venta.idVenta=" + idVenta + ";";
            insert = "INSERT INTO `renta` (`idRenta`, `Estado`, `FKventa`, `FKCliente`, `FKproducto`) VALUES (NULL, 'Renta', '" + idVenta + "', '" + l.getValorId() + "', '" + IdProducto() + "');";
            PreparedStatement pst = cn.prepareStatement(insert);
            PreparedStatement pst2 = cn.prepareStatement(update);

            pst.executeUpdate();
            pst2.executeUpdate();

        } catch (NumberFormatException | SQLException e) {
            System.out.println("Error en insertar " + e.getMessage());
        }

    }

    public void tablaRenta() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Usuario");
        modelo.addColumn("Estado");
        modelo.addColumn("Juego");

        PersonasRenta.setModel(modelo);
        String sql = "";
        sql = "select login.Nombre, login.Apellido, login.Usuario, productos.Nombre, Estado from renta inner join productos on idProducto=FKproducto inner join login on login.idLogin=renta.FKCliente inner join venta on venta.idVenta= FKventa and Estado='Renta';";
        String[] datos = new String[5];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                modelo.addRow(datos);
            }
            PersonasRenta.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println("Error " + ex);
        }
    }

